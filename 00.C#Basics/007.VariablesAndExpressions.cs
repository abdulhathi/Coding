using System;

public class VariablesAndExpressions {
    // Variable     - store some values Ex: (x + " is very good !!!") --> x is variable
    // Literal      - Without using actual value  Ex: (x + " is very good !!!") --> " is very good !!!" is variable
    // Identifier   - The name of the declared variable name Ex: int x = 5, Here x is the identifier
    // Expression   - Evaluate to a value. 
    // Operator     - It works with an operands Ex: +, -, &
    // Operands     - The things can work with operator
    public static void VarsAndExpressions() {
        var x = "Abdul";
        Console.WriteLine(x + " is very good !!!");
    }
}